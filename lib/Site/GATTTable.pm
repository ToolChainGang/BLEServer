#
# ToIEEE11073 - Interpret bytes as IEEE11073 floating point
#
# Inputs:   [Binary] Hex bytes representing IEEE11073 number (ex: [0x02, 0x60, 0x09, 0x00, 0xff])
#
# Outputs:  Number, converted to standard floating point
#
use constant POS_INF => 0x007FFFFE;
use constant NEG_INF => 0x00800002;

our @BadFloat = (
    "+inf",                 # POSITIVE_INFINITY  = 0x007FFFFE,
    "NaN",                  # NAN                = 0x007FFFFF,
    "NaN",                  # NRes               = 0x00800000,
    "NaN",                  # RESERVED_VALUE     = 0x00800001,
    "-inf",                 # NEGAITIVE_INFINITY = 0x00800002,
    );

sub ToIEEE11073 {
    my $Bytes = shift;

    my $Exponent =  $Bytes->[0];
    my $Mantissa = ($Bytes->[1] << 16) + 
                   ($Bytes->[2] <<  8) + 
                   ($Bytes->[3] <<  0);

    my $Output;

    if( $Mantissa >= POS_INF &&
        $Mantissa <= NEG_INF ) {
        $Output = $BadFloat[$Mantissa - POS_INF];
        }
    else {
        $Mantissa = -((0xFFFFFF + 1) - $Mantissa)
            if $Mantissa >= 0x800000;

        $Output = $Mantissa ** $Exponent;
        }

    return $Output;
    }


