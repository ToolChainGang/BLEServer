#
# GetGATTDesc - Return descriptor corresponding to GATT service
#
# Inputs:   Service to check - text 8 hex digits (ex: "00001804")
#
# Outputs:  [Ref to] Gat table descriptor hash
#
sub GetGATTDesc {
    my $UUID = uc shift;

    return { Type => $GATTBad, ID => $UUID, Name => "($UUID: $GATTBad)" }
        unless $UUID =~ m/$UUIDMatch/;

    my $Service = $1;       # From previous match

    my $HighWord = uc substr($Service,0,4);
    my $LowWord  = uc substr($Service,4);

    #
    # Step 1: If it's a known GATT assignment, return that.
    #
    if( $HighWord eq "0000" ) {
        return $GATTTable->{$LowWord}
            if defined $GATTTable->{$LowWord};
        }

    #
    # Step 2: If it's one of the known vendor UUIDs, return that.
    #
    return $VendorTable->{$UUID}
        if defined $VendorTable->{$UUID};
    
    #
    # Step 3: Return one of the "unknown" answers.
    #
    return { Type => $GATTVS, ID => $Service, Name => "($Service: $GATTVS)" }
        if $HighWord ne "0000";

    return { Type => $GATTUK, ID => $Service, Name => "($Service: $GATTUK)" };
    }


